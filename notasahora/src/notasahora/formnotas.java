    private void calcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calcularActionPerformed
   
    if(pnota.getText().equals("") ||
      snota.getText().equals("") || tnota.getText().equals(""))  {
        
        JOptionPane.showMessageDialog(null, "TIENE QUE LLENAR TODAS LAS CASILLAS OBLIGATORIAMENTE");
        
    }  
        
        
        
        
        
    double prinota=Double.parseDouble(pnota.getText());
    double segnota=Double.parseDouble(snota.getText());        
    double ternota=Double.parseDouble(tnota.getText());     
      
    if( prinota>20 || prinota<0 || segnota>20 || segnota<0 || ternota>20 || ternota <0       ){
    
        JOptionPane.showMessageDialog(null, "Digite nÃºmeros positivos y menores a 21");
    }
    else{
        
        
        
        
        
        
        
        
        
        double promedio=(prinota+segnota+ternota)/3;
        
        if(promedio<=10){
            verestado.setText("Desaprobado con nota "+promedio);
            
        }
        else{
            verestado.setText("Aprobado con nota "+promedio);
            
        }
        
    }
        
        
    }//GEN-LAST:event_calcularActionPerformed

