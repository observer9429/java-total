    private void avanzarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_avanzarActionPerformed
        
        accion.damesegundoactual(Integer.parseInt(segundos.getText()));
        accion.plussecond();
        segundos.setText(accion.damesegundoaumentado()+"");
        if(accion.damesegundoaumentado()==60){
            accion.damesegundoactual(0);
           //int segundos=0;
            accion.dameminutosactuales(Integer.parseInt(minutos.getText()));
            accion.plusminutes();
           // accion.damesegundoaumentado();
            segundos.setText(""+accion.damesegundoaumentado());
            minutos.setText(accion.dameminutoaumentado()+"");
        }
        
        if(accion.dameminutoaumentado()==60){
            
            accion.dameminutosactuales(0);
            
            accion.damehorasactuales(Integer.parseInt(horas.getText()));
            accion.plushours();
            minutos.setText(accion.dameminutoaumentado()+"");
            horas.setText(""+accion.damehoraumentada());
        }
        
        if(accion.damehoraumentada()==24){
            accion.damehorasactuales(0);
            horas.setText(accion.damehoraumentada()+"");
            
        }
     
    }//GEN-LAST:event_avanzarActionPerformed

    private void retrocederActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_retrocederActionPerformed
        accion.damesegundoactual(Integer.parseInt(segundos.getText())) ;
        accion.menossegundos();
        segundos.setText(accion.damesegundoaumentado()+"");
        if(accion.damesegundoaumentado()<0){
          accion.damesegundoactual(0);
          segundos.setText(""+accion.damesegundoaumentado());
        }
        
    }//GEN-LAST:event_retrocederActionPerformed

